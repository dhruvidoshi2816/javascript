// while loop

let index = 0

while (index <= 10) {
    //console.log(`value of index is ${index}`);
    index = index + 3
}

let myArray = ["flash", "superman", "batman"]

let arr = 0

while (arr < myArray.length) {
    // console.log(`value is ${myArray[arr]}`);
    arr = arr + 1
}

// do while loop

let score = 11

do {
    console.log(`score is ${score}`);
    score ++
} while (score <= 10);

//  In the world of programming, we often refer to loops using two different control structures. The 'for' and 'while' loops are what we call 'entry control loops' because they check the condition before entering the loop. On the other hand, 'do-while' loops are known as 'exit control loops' since they ensure that the loop body is executed at least once before checking the condition.